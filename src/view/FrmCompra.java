    private void LimpaTable(JTable table) {

        DefaultTableModel model = (DefaultTableModel) table.getModel();

        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }

    }

    private void atualizaTableCompras(ArrayList<DaoCompra> compras) {

        LimpaTable(tblCompra);

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        if (compras.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Não foram encontradas compras "
                    + "no banco de dados.", "Anteção",
                    JOptionPane.INFORMATION_MESSAGE);
        } else {

            DefaultTableModel dadosCompras = (DefaultTableModel) tblCompra.getModel();

            String Linha[] = new String[]{"", "", "", "", "", ""};

            try {

                int posicao = -1;
                for (DaoCompra compra : compras) {
                    posicao++;

                    dadosCompras.addRow(Linha);
                    dadosCompras.setValueAt(compra.getNumCompra(), posicao, 0);
                    dadosCompras.setValueAt(compra.getCodFor(), posicao, 1);
                    dadosCompras.setValueAt(compra.getNome(), posicao, 2);
                    dadosCompras.setValueAt(sdf.format(compra.getDataCompra()), posicao, 3);
                    dadosCompras.setValueAt(sdf.format(compra.getDataEntrega()), posicao, 4);
                    dadosCompras.setValueAt(compra.getObs(), posicao, 5);
                }

            } catch (Exception ex) {

                JOptionPane.showMessageDialog(null, "Ocorreu um erro"
                        + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);

            }

        }

    }

    private void atualizaTableItem(ArrayList<DaoItemCompra> itens) {

        LimpaTable(tblProduto);

        double total = 0;

        if (itens.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Não foram encontrados "
                    + "Itens nessa compra!", "Anteção",
                    JOptionPane.INFORMATION_MESSAGE);
        } else {

            DefaultTableModel dadosItens = (DefaultTableModel) tblProduto.getModel();

            String Linha[] = new String[]{"", "", "", "", ""};

            try {

                int posicao = -1;
                for (DaoItemCompra item : itens) {

                    dadosItens.addRow(Linha);

                    dadosItens.setValueAt(item.getCodPro(), posicao, 0);
                    dadosItens.setValueAt(item.getDescricao(), posicao, 1);
                    dadosItens.setValueAt(item.getQuantidade(), posicao, 2);

                    dadosItens.setValueAt(nfPreco.valueToString(item.getPrecoUnit()), posicao, 3);
                    dadosItens.setValueAt(nfPreco.valueToString(item.getSubtotal()), posicao, 4);

                    total += item.getSubtotal();

                }

                txtTotalCompra.setText(nfPreco.valueToString(total));

            } catch (Exception ex) {

                JOptionPane.showMessageDialog(null, "Ocorreu um erro"
                        + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);

            }

        }

    }

