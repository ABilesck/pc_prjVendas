    private void LimpaTable() {

        DefaultTableModel model = (DefaultTableModel) tblFornecedores.getModel();

        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }

    }

    private void AtualizaTable(ArrayList<DaoFornecedor> fornecedores) {

        LimpaTable();

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        if (!fornecedores.isEmpty()) {

            DefaultTableModel dadosFornecedores = (DefaultTableModel) tblFornecedores.getModel();

            String Linha[] = new String[]{"", "", "", "", "", "", "", "", ""};

            try {

                int pos = -1;

                for (DaoFornecedor fornecedor : fornecedores) {

                    pos++;

                    dadosFornecedores.addRow(Linha);

                    dadosFornecedores.setValueAt(fornecedor.getCodFor(), pos, 0);
                    dadosFornecedores.setValueAt(fornecedor.getRazao(), pos, 1);
                    dadosFornecedores.setValueAt(fornecedor.getFantasia(), pos, 2);
                    dadosFornecedores.setValueAt(fornecedor.getEndereco(), pos, 3);
                    dadosFornecedores.setValueAt(fornecedor.getCidade(), pos, 4);
                    dadosFornecedores.setValueAt(fornecedor.getBairro(), pos, 5);
                    dadosFornecedores.setValueAt(fornecedor.getUf().toUpperCase(), pos, 6);
                    dadosFornecedores.setValueAt(mfCep.valueToString(fornecedor.getCep()), pos, 7);
                    dadosFornecedores.setValueAt(mfTel.valueToString(fornecedor.getTelefone()), pos, 8);
                    dadosFornecedores.setValueAt(mfTel.valueToString(fornecedor.getFax()), pos, 9);
                    dadosFornecedores.setValueAt(mfCnpj.valueToString(fornecedor.getCnpj()), pos, 10);
                    dadosFornecedores.setValueAt(mfIe.valueToString(fornecedor.getIe()), pos, 11);
                    dadosFornecedores.setValueAt(sdf.format(fornecedor.getDatacad()), pos, 12);

                }

                tblFornecedores.setModel(dadosFornecedores);

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro no Banco "
                        + "de dados" + ex.getMessage(),
                        "Erro:", JOptionPane.ERROR_MESSAGE);
            }

        }

    }

