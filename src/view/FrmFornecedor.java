    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        HabilitarBotoes(false);
        HabilitarCampos(true);
        LimpaCampos();
        //txtCodFor.setEnabled(false);
        txtRazao.requestFocus();
        editar = true;
        try {

            if (tblFornecedores.getRowCount() > 0) {

                int posLinha = tblFornecedores.getSelectedRow();

                if (posLinha >= 0) {

                    //txtCodFor.setText(tblFornecedores.getValueAt(posLinha, 0).toString());
                    codigo = Integer.parseInt(tblFornecedores.getValueAt(posLinha, 0).toString());
                    txtRazao.setText(tblFornecedores.getValueAt(posLinha, 1).toString());
                    txtFantasia.setText(tblFornecedores.getValueAt(posLinha, 2).toString());
                    txtEndereco.setText(tblFornecedores.getValueAt(posLinha, 3).toString());
                    txtCidade.setText(tblFornecedores.getValueAt(posLinha, 4).toString());
                    txtBairro.setText(tblFornecedores.getValueAt(posLinha, 5).toString());
                    txtUf.setValue(tblFornecedores.getValueAt(posLinha, 6).toString());
                    txtCep.setValue(tblFornecedores.getValueAt(posLinha, 7).toString());

                    if (tblFornecedores.getValueAt(posLinha, 8).toString()
                            .equals("(__)_____-____")) {

                        txtTelefone.setValue("");

                    } else {

                        txtTelefone.setValue(
                                mfTel.stringToValue(tblFornecedores
                                        .getValueAt(posLinha, 8).toString()).toString());

                    }

                    txtFax.setValue(tblFornecedores.getValueAt(posLinha, 9).toString());
                    txtCnpj.setValue(tblFornecedores.getValueAt(posLinha, 10).toString());
                    txtIe.setValue(tblFornecedores.getValueAt(posLinha, 11).toString());
                    txtDatacad.setValue(tblFornecedores.getValueAt(posLinha, 12).toString());

                } else {

                    JOptionPane.showMessageDialog(null, "Selecione um cliente para alterar",
                            "Erro:", JOptionPane.ERROR_MESSAGE);
                    btnCancelarActionPerformed(null);

                }
            } else {

                JOptionPane.showMessageDialog(null, "Não existem clientes cadastrados!",
                        "Erro:", JOptionPane.ERROR_MESSAGE);
                btnCancelarActionPerformed(null);
            }

        } catch (Exception ex) {

            JOptionPane.showMessageDialog(null, "Ocorreu um erro" + ex.getMessage(),
                    "Erro:", JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_btnAlterarActionPerformed

